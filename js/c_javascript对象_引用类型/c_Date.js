var a= new Date();

a.toJSON();//以josn 形式返回字符串
a.getDate()	//从 Date 对象返回一个月中的某一天 (1 ~ 31)。
a.getDay()	//从 Date 对象返回一周中的某一天 (0 ~ 6)。
a.getFullYear()	//从 Date 对象以四位数字返回年份。
a.getHours()	//返回 Date 对象的小时 (0 ~ 23)。
a.getMilliseconds()	//返回 Date 对象的毫秒(0 ~ 999)。
a.getMinutes()	//返回 Date 对象的分钟 (0 ~ 59)。
a.getMonth()	//从 Date 对象返回月份 (0 ~ 11)。
a.getSeconds()	//返回 Date 对象的秒数 (0 ~ 59)。
a.getTime()	//返回 1970 年 1 月 1 日至今的毫秒数。
a.getTimezoneOffset()	//返回本地时间与格林威治标准时间 (GMT) 的分钟差。
a.getUTCDate()	//根据世界时从 Date 对象返回月中的一天 (1 ~ 31)。
a.getUTCDay()	//根据世界时从 Date 对象返回周中的一天 (0 ~ 6)。
a.getUTCFullYear()	//根据世界时从 Date 对象返回四位数的年份。
a.getUTCHours()	//根据世界时返回 Date 对象的小时 (0 ~ 23)。
a.getUTCMilliseconds()	//根据世界时返回 Date 对象的毫秒(0 ~ 999)。
a.getUTCMinutes()	//根据世界时返回 Date 对象的分钟 (0 ~ 59)。
a.getUTCMonth()	//根据世界时从 Date 对象返回月份 (0 ~ 11)。
a.getUTCSeconds()	//根据世界时返回 Date 对象的秒钟 (0 ~ 59)。
a.toDateString();	//把 Date 对象的日期部分转换为字符串。